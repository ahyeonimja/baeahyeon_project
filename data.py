def calculate_recommendation(destination, intensity, companion, selected_styles):
    """
    사용자 취향 테스트에 따라 세 영화 매칭율 계산
    """
    scores = {"너의 이름은": 0, "날씨의 아이": 0, "스즈메의 문단속": 0}
    
    for movie, weight_data in WEIGHTS.items():
        # Destination score (max 10)
        scores[movie] += weight_data["destination"].get(destination, 0)
        
        # Intensity score (max 10)
        scores[movie] += weight_data["intensity"].get(intensity, 0)
        
        # Companion score (max 10)
        scores[movie] += weight_data["companion"].get(companion, 0)
        
        # Style score (styles selected, each style weight up to 10. We average the weights)
        style_scores = [weight_data["styles"].get(s, 0) for s in selected_styles]
        if style_scores:
            scores[movie] += sum(style_scores) / len(style_scores)
        else:
            scores[movie] += 5.0 # baseline if no style selected
            
    # Convert scores to percentages (min score around 10, max around 35)
    # Let's normalize so they range between 50% and 99% for beautiful UX presentation
    max_score = max(scores.values())
    min_score = min(scores.values())
    
    results = {}
    for movie, raw_score in scores.items():
        # simple interpolation for nice percentage
        if max_score == min_score:
            percentage = 80
        else:
            percentage = int(70 + (raw_score - min_score) / (max_score - min_score) * 28)
        results[movie] = percentage
        
    # Pick the recommended movie (highest percentage)
    recommended_movie = max(results, key=results.get)
    
    return recommended_movie, results
